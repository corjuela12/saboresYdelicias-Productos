                                                    empleado e ON v.empleado_id_empleado = e.id_empleado
                                                JOIN 
                                                    pedido pe ON v.pedido_id_pedido = pe.id_pedido
                                                JOIN 
                                                    cliente c ON pe.cliente_id_cliente = c.id_cliente
                                                JOIN 
                                                    producto p ON pe.producto_id_producto = p.id_producto";
                    
